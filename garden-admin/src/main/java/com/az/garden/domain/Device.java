package com.az.garden.domain;

public class Device {
	private String name;
	private String command;
	
	public String getCommand() {
		return command;
	}
	public String getName() {
		return name;
	}
	public void setCommand(String command) {
		this.command = command;
	}
	public void setName(String name) {
		this.name = name;
	}
}
